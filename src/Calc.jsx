
function add(a,b){
    let sum =a +b;
    return sum;
}
function subract(a,b){
    let diff =a - b;
    return diff;
}
function multiply(a,b){
    let mul =a*b;
    return mul;
}
function divide(a,b){
    let div =a /b;
    return div;
}
function percent(a,b){
    let div =a/100;
    return div*b;
}
export {add,subract, multiply, divide,percent};