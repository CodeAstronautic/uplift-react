
function addition(a,b){
    let sum =a +b;
    return sum;
}
function subtraction(a,b){
    let diff =a - b;
    return diff;
}
function multiplication(a,b){
    let mul =a*b;
    return mul;
}
function division(a,b){
    let div =a /b;
    return div;
}
export {addition,subtraction, multiplication, division};
